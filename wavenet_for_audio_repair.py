def load_audio_pair(clean_path, degraded_path, target_sr=TARGET_SR):
    # Loads a clean/degraded pair, resamples to target_sr, converts to mono,
    # and trims both signals to the same length.
    clean, sr_clean = librosa.load(str(clean_path), sr=target_sr, mono=True)
    degraded, sr_deg = librosa.load(str(degraded_path), sr=target_sr, mono=True)

    min_len = min(len(clean), len(degraded))

    clean = clean[:min_len]
    degraded = degraded[:min_len]

    return clean.astype(np.float32), degraded.astype(np.float32)

class DAPSAudioRestorationDataset(Dataset):
    # Dataset for WaveNet-style audio restoration.
    #
    # Input:
    #     degraded waveform chunk
    #
    # Target:
    #     clean waveform chunk

    def __init__(
        self,
        dataframe,
        chunk_length=CHUNK_LENGTH,
        target_sr=TARGET_SR,
        training=True,
        chunks_per_file=4
    ):
        self.df = dataframe.reset_index(drop=True)
        self.chunk_length = chunk_length
        self.target_sr = target_sr
        self.training = training
        self.chunks_per_file = chunks_per_file

    def __len__(self):
        if self.training:
            return len(self.df) * self.chunks_per_file
        else:
            return len(self.df)

    def __getitem__(self, idx):
        real_idx = idx % len(self.df)
        row = self.df.iloc[real_idx]

        clean, degraded = load_audio_pair(
            row["clean_path"],
            row["degraded_path"],
            target_sr=self.target_sr
        )

        # Pad if audio is shorter than one training chunk
        if len(clean) < self.chunk_length:
            pad_amount = self.chunk_length - len(clean)
            clean = np.pad(clean, (0, pad_amount))
            degraded = np.pad(degraded, (0, pad_amount))

        # Choose chunk start
        max_start = len(clean) - self.chunk_length

        if self.training:
            start = random.randint(0, max_start)
        else:
            start = max_start // 2

        end = start + self.chunk_length

        clean_chunk = clean[start:end]
        degraded_chunk = degraded[start:end]

        # Shape: [channels, time]
        clean_chunk = torch.tensor(clean_chunk).unsqueeze(0)
        degraded_chunk = torch.tensor(degraded_chunk).unsqueeze(0)

        return degraded_chunk, clean_chunk

