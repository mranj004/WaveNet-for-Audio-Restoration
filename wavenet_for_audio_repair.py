def train_one_epoch(model, dataloader, optimizer):
    model.train()

    total_losses = []
    waveform_losses = []
    spectral_losses = []

    for degraded, clean in tqdm(dataloader, desc="Training", leave=False):
        degraded = degraded.to(device)
        clean = clean.to(device)

        optimizer.zero_grad()

        restored = model(degraded)

        loss, waveform_loss, spec_loss = combined_audio_loss(
            restored,
            clean,
            waveform_weight=1.0,
            spectral_weight=0.5
        )

        loss.backward()

        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)

        optimizer.step()

        total_losses.append(loss.item())
        waveform_losses.append(waveform_loss.item())
        spectral_losses.append(spec_loss.item())

    return {
        "total_loss": np.mean(total_losses),
        "waveform_loss": np.mean(waveform_losses),
        "spectral_loss": np.mean(spectral_losses)
    }


def validate_one_epoch(model, dataloader):
    model.eval()

    total_losses = []
    waveform_losses = []
    spectral_losses = []

    with torch.no_grad():
        for degraded, clean in tqdm(dataloader, desc="Validation", leave=False):
            degraded = degraded.to(device)
            clean = clean.to(device)

            restored = model(degraded)

            loss, waveform_loss, spec_loss = combined_audio_loss(
                restored,
                clean,
                waveform_weight=1.0,
                spectral_weight=0.5
            )

            total_losses.append(loss.item())
            waveform_losses.append(waveform_loss.item())
            spectral_losses.append(spec_loss.item())

    return {
        "total_loss": np.mean(total_losses),
        "waveform_loss": np.mean(waveform_losses),
        "spectral_loss": np.mean(spectral_losses)
    }

