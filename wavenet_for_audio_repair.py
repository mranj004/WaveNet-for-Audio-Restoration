def restore_chunk(model, degraded_audio):
    # Restores a single waveform chunk.
    #
    # degraded_audio:
    #     1D numpy array

    model.eval()

    x = torch.tensor(degraded_audio, dtype=torch.float32).unsqueeze(0).unsqueeze(0).to(device)

    with torch.no_grad():
        y_hat = model(x)

    restored = y_hat.squeeze().cpu().numpy()

    return restored

def plot_waveforms(clean, degraded, restored, sr=TARGET_SR, title="Waveform Comparison"):
    min_len = min(len(clean), len(degraded), len(restored))

    clean = clean[:min_len]
    degraded = degraded[:min_len]
    restored = restored[:min_len]

    time_axis = np.arange(min_len) / sr

    plt.figure(figsize=(12, 5))
    plt.plot(time_axis, clean, label="Target Clean", alpha=0.8)
    plt.plot(time_axis, degraded, label="Before: Degraded", alpha=0.6)
    plt.plot(time_axis, restored, label="After: WaveNet Restored", alpha=0.7)
    plt.xlabel("Time in seconds")
    plt.ylabel("Amplitude")
    plt.title(title)
    plt.legend()
    plt.show()

def plot_single_spectrogram(y, sr=TARGET_SR, title="Spectrogram"):
    D = librosa.amplitude_to_db(
        np.abs(librosa.stft(y, n_fft=1024, hop_length=256)),
        ref=np.max
    )

    plt.figure(figsize=(10, 4))
    librosa.display.specshow(
        D,
        sr=sr,
        hop_length=256,
        x_axis="time",
        y_axis="hz"
    )
    plt.colorbar(format="%+2.0f dB")
    plt.title(title)
    plt.tight_layout()
    plt.show()

def plot_restoration_spectrograms(clean, degraded, restored, sr=TARGET_SR):
    plot_single_spectrogram(clean, sr, "Target Clean Spectrogram")
    plot_single_spectrogram(degraded, sr, "Before: Degraded Spectrogram")
    plot_single_spectrogram(restored, sr, "After: WaveNet Restored Spectrogram")

