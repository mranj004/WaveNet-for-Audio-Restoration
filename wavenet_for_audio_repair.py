def compute_audio_features(path, target_sr=TARGET_SR):
    # Loads an audio file and computes simple waveform/spectral features.
    y, sr = librosa.load(path, sr=target_sr, mono=True)

    rms = np.sqrt(np.mean(y ** 2))
    peak = np.max(np.abs(y))
    clipping_percent = np.mean(np.abs(y) > 0.99) * 100

    zcr = librosa.feature.zero_crossing_rate(y)[0].mean()

    spectral_centroid = librosa.feature.spectral_centroid(y=y, sr=sr)[0].mean()
    spectral_bandwidth = librosa.feature.spectral_bandwidth(y=y, sr=sr)[0].mean()

    return {
        "rms": rms,
        "peak_amplitude": peak,
        "clipping_percent": clipping_percent,
        "zero_crossing_rate": zcr,
        "spectral_centroid": spectral_centroid,
        "spectral_bandwidth": spectral_bandwidth
    }


