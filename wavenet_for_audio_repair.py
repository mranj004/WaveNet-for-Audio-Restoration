# 5. Loss Functions
# Waveform L1 Loss + Spectral Magnitude Loss

def spectral_magnitude_loss(pred, target, n_fft=512, hop_length=128):
    # Computes L1 loss between log-magnitude spectrograms.
    #
    # pred and target shape:
    #     [batch, 1, time]

    pred = pred.squeeze(1)
    target = target.squeeze(1)

    window = torch.hann_window(n_fft).to(pred.device)

    pred_stft = torch.stft(
        pred,
        n_fft=n_fft,
        hop_length=hop_length,
        window=window,
        return_complex=True
    )

    target_stft = torch.stft(
        target,
        n_fft=n_fft,
        hop_length=hop_length,
        window=window,
        return_complex=True
    )

    pred_mag = torch.abs(pred_stft)
    target_mag = torch.abs(target_stft)

    loss = F.l1_loss(
        torch.log1p(pred_mag),
        torch.log1p(target_mag)
    )

    return loss


def combined_audio_loss(
    pred,
    target,
    waveform_weight=1.0,
    spectral_weight=0.5
):
    # Combined restoration loss.
    #
    # Total loss =
    #     waveform_weight * waveform L1 loss
    #     +
    #     spectral_weight * spectral magnitude loss

    waveform_loss = F.l1_loss(pred, target)
    spec_loss = spectral_magnitude_loss(pred, target)

    total_loss = waveform_weight * waveform_loss + spectral_weight * spec_loss

    return total_loss, waveform_loss, spec_loss

