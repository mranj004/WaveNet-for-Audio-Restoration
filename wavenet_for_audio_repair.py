# 4. WaveNet-Style Architecture
# Dilated 1D Convolutions + Gated Activations
# Residual and Skip Connections

class WaveNetResidualBlock(nn.Module):
    # One WaveNet-style residual block.
    #
    # Uses:
    # - dilated 1D convolutions
    # - gated activation: tanh(filter) * sigmoid(gate)
    # - residual connection
    # - skip connection

    def __init__(self, residual_channels, skip_channels, dilation, kernel_size=3):
        super().__init__()

        padding = dilation * (kernel_size - 1) // 2

        self.filter_conv = nn.Conv1d(
            residual_channels,
            residual_channels,
            kernel_size=kernel_size,
            padding=padding,
            dilation=dilation
        )

        self.gate_conv = nn.Conv1d(
            residual_channels,
            residual_channels,
            kernel_size=kernel_size,
            padding=padding,
            dilation=dilation
        )

        self.residual_conv = nn.Conv1d(
            residual_channels,
            residual_channels,
            kernel_size=1
        )

        self.skip_conv = nn.Conv1d(
            residual_channels,
            skip_channels,
            kernel_size=1
        )

    def forward(self, x):
        filter_out = torch.tanh(self.filter_conv(x))
        gate_out = torch.sigmoid(self.gate_conv(x))

        gated = filter_out * gate_out

        residual = self.residual_conv(gated)
        skip = self.skip_conv(gated)

        output = x + residual

        return output, skip

class WaveNetRestorationModel(nn.Module):
    # WaveNet-style model for audio restoration.
    #
    # Input:
    #     degraded waveform [batch, 1, time]
    #
    # Output:
    #     restored waveform [batch, 1, time]

    def __init__(
        self,
        in_channels=1,
        residual_channels=64,
        skip_channels=128,
        num_blocks=3,
        num_layers=8,
        kernel_size=3
    ):
        super().__init__()

        self.input_conv = nn.Conv1d(
            in_channels,
            residual_channels,
            kernel_size=1
        )

        self.blocks = nn.ModuleList()

        for _ in range(num_blocks):
            for layer in range(num_layers):
                dilation = 2 ** layer

                self.blocks.append(
                    WaveNetResidualBlock(
                        residual_channels=residual_channels,
                        skip_channels=skip_channels,
                        dilation=dilation,
                        kernel_size=kernel_size
                    )
                )

        self.output_network = nn.Sequential(
            nn.ReLU(),
            nn.Conv1d(skip_channels, skip_channels, kernel_size=1),
            nn.ReLU(),
            nn.Conv1d(skip_channels, in_channels, kernel_size=1),
            nn.Tanh()
        )

    def forward(self, x):
        x = self.input_conv(x)

        skip_connections = []

        for block in self.blocks:
            x, skip = block(x)
            skip_connections.append(skip)

        total_skip = sum(skip_connections)

        out = self.output_network(total_skip)

        return out

