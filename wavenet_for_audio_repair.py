# Evaluation Metrics
# MSE, MAE, SNR, SI-SDR

def compute_mse(pred, target):
    return np.mean((pred - target) ** 2)


def compute_mae(pred, target):
    return np.mean(np.abs(pred - target))


def compute_snr(pred, target, eps=1e-8):
    # Signal-to-noise ratio comparing prediction to target.
    # Higher is better.
    noise = target - pred

    signal_power = np.sum(target ** 2)
    noise_power = np.sum(noise ** 2)

    return 10 * np.log10((signal_power + eps) / (noise_power + eps))


def compute_si_sdr(pred, target, eps=1e-8):
    # Scale-Invariant Signal-to-Distortion Ratio.
    #
    # Higher is better.

    pred = pred - np.mean(pred)
    target = target - np.mean(target)

    target_energy = np.sum(target ** 2) + eps

    scale = np.sum(pred * target) / target_energy

    target_scaled = scale * target
    error = pred - target_scaled

    ratio = np.sum(target_scaled ** 2) / (np.sum(error ** 2) + eps)

    return 10 * np.log10(ratio + eps)

def evaluate_model_on_loader(model, dataloader):
    model.eval()

    rows = []

    with torch.no_grad():
        for degraded, clean in tqdm(dataloader, desc="Evaluating"):
            degraded = degraded.to(device)
            clean = clean.to(device)

            restored = model(degraded)

            degraded_np = degraded.cpu().numpy()
            clean_np = clean.cpu().numpy()
            restored_np = restored.cpu().numpy()

            batch_size = degraded_np.shape[0]

            for i in range(batch_size):
                degraded_i = degraded_np[i, 0]
                clean_i = clean_np[i, 0]
                restored_i = restored_np[i, 0]

                rows.append({
                    "degraded_mse": compute_mse(degraded_i, clean_i),
                    "restored_mse": compute_mse(restored_i, clean_i),

                    "degraded_mae": compute_mae(degraded_i, clean_i),
                    "restored_mae": compute_mae(restored_i, clean_i),

                    "degraded_snr": compute_snr(degraded_i, clean_i),
                    "restored_snr": compute_snr(restored_i, clean_i),

                    "degraded_si_sdr": compute_si_sdr(degraded_i, clean_i),
                    "restored_si_sdr": compute_si_sdr(restored_i, clean_i)
                })

    return pd.DataFrame(rows)


